    private void btnResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultadoActionPerformed
        AreasFigurasGeometricas areaFigura = new AreasFigurasGeometricas();
        try {
            if (!txtLado.getText().isEmpty()) {
                double lado = Integer.parseInt(txtLado.getText());
                areaFigura.AreaCuadrado(lado);
                txtAreaCuadrado.setText(String.valueOf(areaFigura.AreaCuadrado(lado)));
            } else {
                JOptionPane.showMessageDialog(null, "falta ingresar numero");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "el formato debe de ser un numero");
        }
    }//GEN-LAST:event_btnResultadoActionPerformed

